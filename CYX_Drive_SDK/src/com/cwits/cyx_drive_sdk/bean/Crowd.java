package com.cwits.cyx_drive_sdk.bean;

import java.io.Serializable;

/**
 * 单个的交通信息实体
 * @author Administrator
 *
 */
public class Crowd implements Serializable {

	private static final long serialVersionUID = 1L;
	
	private String crowdId;	//id
	private String type;	//事件类型，0：堵车，1：事故，2：施工，3：管制
	private String createTime;	//otc时间
	private double lon;	//经度，精度为5位小数
	private double lat;		//纬度，精度为5位小数
	public String getCrowdId() {
		return crowdId;
	}
	public void setCrowdId(String crowdId) {
		this.crowdId = crowdId;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getCreateTime() {
		return createTime;
	}
	public void setCreateTime(String createTime) {
		this.createTime = createTime;
	}
	public double getLon() {
		return lon;
	}
	public void setLon(double lon) {
		this.lon = lon;
	}
	public double getLat() {
		return lat;
	}
	public void setLat(double lat) {
		this.lat = lat;
	}
	public static long getSerialversionuid() {
		return serialVersionUID;
	}

	
}
